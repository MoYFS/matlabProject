close all;
clear;
n=1:40;
a=2;b=3;
x1=cos(2*pi*0.1*n);
x2=cos(2*pi*0.4*n);
x=a*x1+b*x2;
num=[1,-2];
den=[1,-0.4,0.3];
y1=filter(num,den,x1);
y2=filter(num,den,x2);
%filter函数用来求系统的响应
y=filter(num,den,x);
yt=a*y1+b*y2;
figure;
subplot(2,1,1);
stem(n,y);
title('输出y的振幅');
subplot(2,1,2);
stem(n,yt);
title('输出a*y1+b*y2的振幅');

D=9;
a1=3;b1=5;
x3=a1*cos(2*pi*0.1*n)+b1*cos(2*pi*0.4*n);
xd=[zeros(1,D),x3];
y3=filter(num,den,x3);
yd=filter(num,den,xd);
figure;
subplot(2,1,1);
stem(y3);
title('输入为x(n)时的输出');
subplot(2,1,2);
stem(yd);
title('输入为x(n-D)时的输出');