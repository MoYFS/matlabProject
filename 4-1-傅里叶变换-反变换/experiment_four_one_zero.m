clc;
close all;
Image=imread(".\lena.jpg");
r=Image(:,:,1);
g=Image(:,:,2);
b=Image(:,:,3);
%傅里叶变换
fr=fft2(r);
fg=fft2(g);
fb=fft2(b);
fImage=fft2(Image);
%傅里叶反变换
or=ifft2(fr);
og=ifft2(fg);
ob=ifft2(fb);
%合成一张图
out(:,:,1)=or;
out(:,:,2)=og;
out(:,:,3)=ob;
fImage=ifft2(fImage);
figure;
subplot(1,3,1);imshow(Image);title('原图');
subplot(1,3,2),imshow(uint8(out)),title('分层反变换合成图');
subplot(1,3,3),imshow(uint8(fImage)),title('整体反变换图');
